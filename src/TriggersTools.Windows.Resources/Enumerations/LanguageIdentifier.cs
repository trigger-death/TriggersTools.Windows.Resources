	///  Language identifiers used in <see cref="UpdateResource"/>.
	/// </summary>
	public enum LanguageIdentifier : ushort {
		/// <summary>Default custom locale language</summary>
		Neutral = 0x0C00,
		/// <summary>English (en)</summary>
		English = 0x0C09,
		/// <summary>Japanese (ja)</summary>
		Japanese = 0x0411,
	}
}
